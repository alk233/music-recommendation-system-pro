def render_offline_metrics_glossary(in_expander: bool = True, expanded: bool = False):
    """展示离线评测指标释义表。"""
    df = offline_metrics_glossary_dataframe()
    if in_expander:
        with st.expander("离线评测指标释义", expanded=expanded):
            st.dataframe(df, use_container_width=True, hide_index=True)
    else:
        st.markdown("### 离线评测指标释义")
        st.dataframe(df, use_container_width=True, hide_index=True)


