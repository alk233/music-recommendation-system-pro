def render_page_metric_note(page_key: str, expanded: bool = False):
    """page_key: home | cf | ncf | content | hybrid | analysis | compare"""
    body = _PAGE.get(page_key, "").strip()
    text = _Z if not body else f"{_Z}\n\n{body}"
    with st.expander("指标说明", expanded=expanded):
        st.markdown(text)


def render_log_normalized_preference_note(expanded: bool = False):
    render_page_metric_note("analysis", expanded=expanded)
